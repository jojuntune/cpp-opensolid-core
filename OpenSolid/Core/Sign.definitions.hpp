#pragma once

#include <OpenSolid/config.hpp>

#include <OpenSolid/Core/Sign.declarations.hpp>

namespace opensolid
{
    class Sign
    {
    private:
        int _value;

        explicit
        Sign(int value);
    public:
        Sign();

        int
        value() const;

        operator int() const;

        bool
        operator==(Sign other) const;

        bool
        operator!=(Sign other) const;

        Sign
        operator-() const;

        Sign
        operator*(Sign other) const;

        static Sign
        ZERO();
        
        static Sign
        POSITIVE();
        
        static Sign
        NEGATIVE();

        static Sign
        of(double value);
    };
}
